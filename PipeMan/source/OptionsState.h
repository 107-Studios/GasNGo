#ifndef	_OPTIONSSTATE_H_
#define	_OPTIONSSTATE_H_

#include "IGameState.h"

class COptionsState : public IGameState
{
	enum {AUDIO, GAMEPLAY, CONTROLS, VIDEO, EXIT, OK, CANCEL, DEFAULT, NUM_OPTIONS};

	// Make a true singleton
	COptionsState( char* szMSFName );
	COptionsState(const COptionsState& pState);
	COptionsState& operator=(const COptionsState& pState);
	~COptionsState();

public:
	//////////////////////////////////////////////////////////////////////////
	// GetInstance():		Gets the static instance of the state
	//						returning a pointer to the instance
	//
	// Return:				COptionsState*
	//////////////////////////////////////////////////////////////////////////
	static COptionsState* GetInstance();

	//////////////////////////////////////////////////////////////////////////
	// Enter():				Enters the game state setting up all needed
	//						information based on state
	//
	// Return:				void
	//////////////////////////////////////////////////////////////////////////
	void Enter();

	//////////////////////////////////////////////////////////////////////////
	// Exit():				Exits the game state shutting down all
	//						initialized objects and resets all data
	//
	// Return:				void
	//////////////////////////////////////////////////////////////////////////
	void Exit();

	//////////////////////////////////////////////////////////////////////////
	// Input():				Receives input from the user.
	//
	// Return:				void
	//////////////////////////////////////////////////////////////////////////
	void Input();

	//////////////////////////////////////////////////////////////////////////
	// Update():			Updates the state based on the time passed in
	//						Updates all stored objects based on this time
	//
	// In:					fElapsedTime
	//	
	// Return:				bool
	//////////////////////////////////////////////////////////////////////////
	bool Update(float fElapsedTime);

	//////////////////////////////////////////////////////////////////////////
	// Render()				Renders information to the screen using
	//						the rendering engine.
	//
	// Return:				void
	//////////////////////////////////////////////////////////////////////////
	void Render();

};

//////////////////////////////////////////////////////////////////////////
// Trigger Functions
//			Methods called when the buttons from the IGameState are pressed
//
//
// Return:				bool - Exits the state from IGameState
//////////////////////////////////////////////////////////////////////////
bool Trigger_Options_Exit( IGameState* pThisState );
bool Trigger_Options_SavenExit( IGameState* pThisState );

#endif